//provides a text field to the user upon selecting the radio button
    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here:
        if (PSRadioButton.isSelected()) {
            PS.setDay(DayTextField.getText());
            PS.setModule(ModuleTextField.getText());
            PS.setTime(Integer.parseInt(TimeTextField.getText()));
            PS.setDuration(Integer.parseInt(DurationTextField.getText()));
            PS.setTopic(topicTextField.getText());
            StudyActivities.add(PS);

        } else if (GPSRadioButton.isSelected()) {
            GPS.setDay(DayTextField.getText());
            GPS.setModule(ModuleTextField.getText());
            GPS.setTime(Integer.parseInt(TimeTextField.getText()));
            GPS.setDuration(Integer.parseInt(DurationTextField.getText()));
            GPS.setTeammates(teammatesTextField.getText());
            GPS.setAssessmentname(AssessmentTextField.getText());
            StudyActivities.add(GPS);
    }//GEN-LAST:event_AddButtonActionPerformed
}
