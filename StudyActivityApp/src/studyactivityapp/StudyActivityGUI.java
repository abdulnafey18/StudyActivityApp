//Saves the entered data into activitylog.dat file
    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        // TODO add your handling code here:
        File outFile;
        FileOutputStream fStream;
        ObjectOutputStream oStream;

        try{
            outFile = new File("activitylog.dat");
            fStream = new FileOutputStream(outFile);
            oStream = new ObjectOutputStream(fStream);

            oStream.writeObject(StudyActivities);
            JOptionPane.showMessageDialog(null,"Activity saved");
        } catch (IOException e) {
            System.out.println("Error saving activity"+e);
        }
    }//GEN-LAST:event_SaveButtonActionPerformed
//Loads activitylog.dat file automatically upon starting the GUI application and displays the stored logs in the file    
    public final void loadData(){
        File inFile;
        FileInputStream fStream;
        ObjectInputStream oStream;

        try{
            inFile = new File("activitylog.dat");
            fStream = new FileInputStream(inFile);
            oStream = new ObjectInputStream(fStream);

            StudyActivities = (ArrayList<StudyActivity>) oStream.readObject();
            for(StudyActivity PS:StudyActivities){   
                JOptionPane.showMessageDialog(null, PS.getDetails());   
            }
            oStream.close();
            
        } 
        catch(IOException e){
            System.out.println("Error saving book"+e);
        }
        catch(ClassNotFoundException ex){
            System.out.println(ex);
        }
    }
//Deletes the last activity added
    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        if (StudyActivities.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Sorry, there are no Study Activity logs to delete");
        } 
        else {
            StudyActivities.remove(StudyActivities.size()-1);                   
        }
    }//GEN-LAST:event_DeleteButtonActionPerformed

